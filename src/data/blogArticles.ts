export const getArticleById = (id: string): BlogArticle | undefined => {
    return blogArticles.find(article => article.id === id);
};

// Get articles content for BlogArticle page
export const getArticlesContent = (): Record<string, BlogArticle> => {
    return blogArticles.reduce((acc, article) => {
        acc[article.id] = article;
        return acc;
    }, {} as Record<string, BlogArticle>);
};
